//Escreva um programa que, dado um número de 1 a 7, imprima o dia da semana correspondente (1 = Domingo, 2 = Segunda, etc.).

let dia = 6;

switch (dia) {
    case 1:
        console.log("Domingo");
        break;
    
    case 2:
        console.log("Segunda-Feira");
        break;

    case 3:
        console.log("Terça-Feira");
        break;
        
    case 4:
        console.log("Quarta-Feira");
        break;
        
    case 5:
        console.log("Quinta-Feira");
        break;
        
    case 6:
        console.log("Sexta-Feira");
        break;
    case 7:
        console.log("Sábado");
        break;
    default:
        console.log("Dia inválido.")
}
//Sexta-Feira
